//do-while loop:- it is similar to while loop but the only difference is that in do-while loop the body of the loop will execute at least once even if the condition is false.
#include<stdio.h>
int main(){
    int i=77;
    do{
        printf("%d Rajput\n",i);
        i--;
    }while(i>=0);
     return 0;  
}
